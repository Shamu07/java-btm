class Sofa 
{
    int length;
    int width;
    int height;
    String color;
    String material;
    int numOfChairs;
    int capacity;
    double price;

Sofa(int length, int width, int height, String color, String material,int numOfChairs,int capacity,double price)
{
        this.length = length;
        this.width = width;
        this.height = height;
        this.color = color;
        this.material = material;
        this.numOfChairs= numOfChairs;
        this.capacity = capacity;
        this.price = price;
}
}